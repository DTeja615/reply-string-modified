package com.beta.model;

public class V2ReplyModel {
    private char[] inputRuleArray;
    private String inputString;
    private String responseString;

    public char[] getInputRuleArray() {
        return inputRuleArray;
    }

    public void setInputRuleArray(char[] inputRuleArray) {
        this.inputRuleArray = inputRuleArray;
    }

    public String getInputString() {
        return inputString;
    }

    public void setInputString(String inputString) {
        this.inputString = inputString;
    }

    public String getResponseString() {
        return responseString;
    }

    public void setResponseString(String responseString) {
        this.responseString = responseString;
    }
}
